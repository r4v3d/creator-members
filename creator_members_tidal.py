def verificar_boton_suscribirse_habilitado(page) -> tuple[bool, bool]:
    """
    Verifica el estado del botón Suscríbete.
    Retorna (visible, habilitado).
    - visible: el botón existe y está en la página
    - habilitado: el botón está activo (campos completos) o deshabilitado (faltan campos)
    """
    try:
        if page.is_closed():
            return False, False
    except PWError:
        return False, False

    try:
        btn = page.locator('button:has-text("Suscríbete"), button:has-text("Subscribe")').first
        if not btn.count():
            return False, False

        visible = btn.is_visible(timeout=200)
        if not visible:
            return False, False

        habilitado = btn.is_enabled(timeout=200)
        return True, habilitado
    except Exception:
        return False, False


def corregir_campos_pendientes(page, password: str) -> dict[str, bool]:
    """
    Detecta qué campos faltan y los rellena rápidamente.
    Retorna el estado después de la corrección.
    """
    estado_final = {"password": False, "fecha_nacimiento": False, "terminos": False, "boton_suscribirse": False}
    intentos = 0
    max_intentos = 2

    while intentos < max_intentos:
        intentos += 1
        estado = verificar_registro_completo(page)
        cambios = False

        # 1. Corregir contraseña si falta
        if not estado["password"] and password:
            try:
                loc = page.locator('input[type="password"]').first
                if loc.count() and loc.is_visible(timeout=200):
                    val = loc.input_value(timeout=150)
                    if not val:
                        loc.fill(password, timeout=1000, force=True)
                        cambios = True
                    else:
                        estado_final["password"] = True
                else:
                    estado_final["password"] = True
            except Exception:
                pass
        else:
            estado_final["password"] = True

        # 2. Corregir fecha de nacimiento si falta
        if not estado["fecha_nacimiento"]:
            import random
            dia = str(random.randint(1, 28))
            mes = str(random.randint(1, 12))
            año = str(random.randint(1970, 2005))

            selects = page.locator('select').all()
            if len(selects) >= 3:
                try:
                    # Intentar asignar valores a los selects visibles
                    for i, sel in enumerate(selects[:3]):
                        try:
                            val = sel.input_value(timeout=100)
                            if not val or not val.strip():
                                if i == 0:
                                    sel.select_option(dia)
                                elif i == 1:
                                    sel.select_option(mes)
                                else:
                                    sel.select_option(año)
                                cambios = True
                        except Exception:
                            continue
                except Exception:
                    pass
            else:
                estado_final["fecha_nacimiento"] = True
        else:
            estado_final["fecha_nacimiento"] = True

        # 3. Corregir términos si faltan
        if not estado["terminos"]:
            try:
                # Buscar checkbox no marcado
                checkboxes = page.locator('input[type="checkbox"]').all()
                for cb in checkboxes:
                    try:
                        if not cb.is_checked():
                            cb.check(timeout=500)
                            cambios = True
                    except Exception:
                        continue
            except Exception:
                pass
        else:
            estado_final["terminos"] = True

        # Verificar si el botón se habilitó (inlineado)
        habilitado = False
        try:
            btn = page.locator('button:has-text("Suscríbete"), button:has-text("Subscribe")').first
            if btn.count() and btn.is_visible(timeout=150):
                habilitado = btn.is_enabled(timeout=100)
        except Exception:
            pass
        
        if habilitado:
            estado_final["boton_suscribirse"] = True
            break

        if not cambios:
            break  # No se pudo hacer más cambios

        time.sleep(0.2)  # Pequeña espera entre intentos

    return estado_final


def fase_5_completar_registro(sesiones: list[dict], passwords: list[str]) -> int:
    """
    Fase 5: Rellena contraseña, fecha de nacimiento, acepta términos y pulsa Suscríbete.
    Si el botón está deshabilitado, intenta corregir campos faltantes automáticamente.
    Retorna el número de ventanas completadas exitosamente.
    """
    sesiones_validas = [s for s in sesiones if s.get("page")]
    if not sesiones_validas:
        print("No hay ventanas abiertas para completar registro.")
        return 0

    print(f"\n=== FASE 5: Completando registro en {len(sesiones_validas)} ventana(s) ===\n")

    exitosos = 0
    for i, sesion in enumerate(sesiones_validas):
        page = sesion["page"]
        password = passwords[i] if i < len(passwords) else ""

        try:
            # 1. Rellenar contraseña (intento inicial)
            if password and rellenar_contraseña_login(page, password):
                pass  # Éxito, no mostrar aún
            else:
                print(f"[{sesion['n']}] {sesion['perfil']}: no se pudo rellenar contraseña")
                continue

            time.sleep(0.2)

            # 2. Rellenar fecha de nacimiento (intento inicial)
            rellenar_fecha_nacimiento(page)
            time.sleep(0.2)

            # 3. Aceptar términos (intento inicial)
            aceptar_terminos_privacidad(page)
            time.sleep(0.2)

            # 4. Verificar estado del botón Suscríbete (inlineado)
            visible = False
            habilitado = False
            try:
                btn = page.locator('button:has-text("Suscríbete"), button:has-text("Subscribe")').first
                if btn.count():
                    visible = btn.is_visible(timeout=200)
                    if visible:
                        habilitado = btn.is_enabled(timeout=200)
            except Exception:
                pass

            if not visible:
                print(f"[{sesion['n']}] {sesion['perfil']}: botón 'Suscríbete' no visible")
                continue

            # 5. Si botón está deshabilitado, intentar corregir automáticamente
            if not habilitado:
                print(f"[{sesion['n']}] {sesion['perfil']}: botón deshabilitado → corrigiendo...", end=" ")
                estado_corregido = corregir_campos_pendientes(page, password)

                # Verificar nuevamente (inlineado)
                habilitado_post = False
                try:
                    btn = page.locator('button:has-text("Suscríbete"), button:has-text("Subscribe")').first
                    if btn.count() and btn.is_visible(timeout=200):
                        habilitado_post = btn.is_enabled(timeout=200)
                except Exception:
                    pass

                if habilitado_post:
                    print("✓ corregido")
                else:
                    faltantes = []
                    if not estado_corregido["password"]:
                        faltantes.append("contraseña")
                    if not estado_corregido["fecha_nacimiento"]:
                        faltantes.append("fecha")
                    if not estado_corregido["terminos"]:
                        faltantes.append("términos")
                    print(f"✗ sigue bloqueado (falta: {', '.join(faltantes)})")
                    continue

            # 6. Botón habilitado, proceder a pulsar
            if pulsar_suscribirse(page):
                print(f"[{sesion['n']}] {sesion['perfil']}: registro completado ✓")
                exitosos += 1
            else:
                print(f"[{sesion['n']}] {sesion['perfil']}: error al pulsar 'Suscríbete'")

        except Exception as e:
            print(f"[{sesion['n']}] {sesion['perfil']}: error - {e}")

        time.sleep(0.2)

    print(f"\n✓ Fase 5 completada: {exitosos}/{len(sesiones_validas)} registro(s) completado(s)")
    return exitosos


def fase_6_verificar_y_navegar_suscripcion(sesiones: list[dict], emails: list[str], passwords: list[str]) -> int:
    """
    Fase 6: Verifica que todas las ventanas completaron las fases 1-5.
    Si una ventana se recargó y está incompleta, la recupera fase por fase.
    Finalmente navega al link de suscripción en todas las ventanas listas.
    Retorna el número de ventanas que navegaron exitosamente al link.
    """
    link_suscripcion = leer_link_suscripcion()
    if not link_suscripcion:
        print("\n⚠ ERROR: No se encontró link de suscripción en 'link subscription.txt'")
        print("  Crea el archivo con el URL de suscripción de TIDAL.")
        return 0

    sesiones_validas = [s for s in sesiones if s.get("page")]
    if not sesiones_validas:
        print("No hay ventanas abiertas para navegar a suscripción.")
        return 0

    print(f"\n=== FASE 6: Verificando y navegando a suscripción ===")
    print(f"Link: {link_suscripcion}\n")

    exitosos = 0
    recuperados = 0

    for i, sesion in enumerate(sesiones_validas):
        page = sesion["page"]
        email = emails[i] if i < len(emails) else ""
        password = passwords[i] if i < len(passwords) else ""
        n, perfil = sesion["n"], sesion["perfil"]

        try:
            # 1. Detectar estado actual de la ventana
            estado = detectar_estado_ventana(page, email, password)

            # Si está en página de login incompleta, necesita recuperación completa
            if estado["pagina_login"] and not estado["email_rellenado"]:
                print(f"[{n}] {perfil}: ventana recargada → recuperando...", end=" ")
                if recuperar_ventana(sesion, email, password):
                    print("✓ recuperada")
                    recuperados += 1
                else:
                    print("✗ falló recuperación")
                    continue

            # Si está en página de registro incompleta
            elif estado["pagina_registro"] and not estado["boton_suscribirse_habilitado"]:
                print(f"[{n}] {perfil}: registro incompleto → completando...", end=" ")
                if recuperar_ventana(sesion, email, password):
                    print("✓ completado")
                    recuperados += 1
                else:
                    print("✗ falló")
                    continue

            # Verificar nuevamente estado después de posible recuperación
            estado = detectar_estado_ventana(page, email, password)

            # Si ya pasó el registro o el botón está habilitado, está listo
            if estado["registro_completado"] or estado["boton_suscribirse_habilitado"]:
                # Navegar al link de suscripción
                try:
                    page.goto(link_suscripcion, timeout=15000, wait_until="domcontentloaded")
                    print(f"[{n}] {perfil}: navegado a suscripción ✓")
                    exitosos += 1
                except Exception as e:
                    print(f"[{n}] {perfil}: error al navegar - {e}")
            else:
                print(f"[{n}] {perfil}: no se pudo completar el registro")

        except Exception as e:
            print(f"[{n}] {perfil}: error - {e}")

        time.sleep(0.3)

    print(f"\n✓ Fase 6 completada: {exitosos}/{len(sesiones_validas)} navegados ({recuperados} recuperados)")
    return exitosos


def fase_7_activar_prueba_60_dias(sesiones: list[dict]) -> int:
    """
    Fase 7: En la página de suscripción, activa el toggle de
    "Amplía tu periodo de prueba 60 días por solo $76" y pulsa Continuar.
    Retorna el número de ventanas completadas exitosamente.
    """
    sesiones_validas = [s for s in sesiones if s.get("page")]
    if not sesiones_validas:
        print("No hay ventanas abiertas para activar prueba de 60 días.")
        return 0

    print(f"\n=== FASE 7: Activando prueba de 60 días en {len(sesiones_validas)} ventana(s) ===\n")

    exitosos = 0
    for sesion in sesiones_validas:
        page = sesion["page"]
        n, perfil = sesion["n"], sesion["perfil"]

        try:
            # 1. Esperar a que cargue la página de planes
            time.sleep(2.0)

            # 2. Buscar y activar el toggle de 60 días
            # El toggle está cerca del texto "60 días" o "Amplía tu periodo de prueba"
            toggle_activado = False

            # Intentar por texto cercano
            try:
                # Buscar el contenedor/label que mencione 60 días
                loc_60dias = page.locator('text=/60 días|60 days/i').first
                if loc_60dias.count():
                    # Buscar el toggle/switch cercano
                    toggle = loc_60dias.locator(
                        'xpath=ancestor::div[contains(@class, "switch")] | '
                        'xpath=ancestor::div[contains(@class, "toggle")] | '
                        'xpath=following::div[contains(@class, "switch")][1] | '
                        'xpath=preceding::div[contains(@class, "switch")][1] | '
                        'xpath=ancestor::div[1]//input[@type="checkbox"] | '
                        'xpath=ancestor::div[1]//button[contains(@role, "switch")]'
                    ).first

                    if toggle.count():
                        # Verificar si ya está activado
                        is_checked = False
                        try:
                            if toggle.input_value(timeout=100) or toggle.is_checked():
                                is_checked = True
                        except Exception:
                            pass

                        if not is_checked:
                            toggle.click(timeout=1500)
                            toggle_activado = True
                            print(f"[{n}] {perfil}: toggle 60 días activado ✓")
                        else:
                            toggle_activado = True
                            print(f"[{n}] {perfil}: toggle 60 días ya estaba activado ✓")
            except Exception:
                pass

            # Si no se encontró por texto, intentar por selectores comunes de toggle
            if not toggle_activado:
                selectores_toggle = [
                    'input[type="checkbox"]:not(:checked)',
                    'button[role="switch"][aria-checked="false"]',
                    '[class*="switch"][role="switch"][aria-checked="false"]',
                    '[class*="toggle"]:not(:checked)',
                ]
                for sel in selectores_toggle:
                    try:
                        toggle = page.locator(sel).first
                        if toggle.count() and toggle.is_visible(timeout=200):
                            toggle.click(timeout=1000)
                            toggle_activado = True
                            print(f"[{n}] {perfil}: toggle 60 días activado ✓")
                            break
                    except Exception:
                        continue

            if not toggle_activado:
                print(f"[{n}] {perfil}: no se encontró toggle de 60 días (podría estar ya activo)")

            time.sleep(0.5)

            # 3. Pulsar botón Continuar
            boton_continuar = False
            patrones_continuar = (
                re.compile(r"^\s*continuar\s*$", re.I),
                re.compile(r"^\s*continue\s*$", re.I),
                re.compile(r"^\s*siguiente\s*$", re.I),
                re.compile(r"^\s*next\s*$", re.I),
            )

            for patron in patrones_continuar:
                try:
                    btn = page.get_by_role("button", name=patron).first
                    if btn.count() and btn.is_visible(timeout=300):
                        btn.click(timeout=2000)
                        boton_continuar = True
                        print(f"[{n}] {perfil}: botón 'Continuar' pulsado ✓")
                        break
                except Exception:
                    continue

            # Intentar por selector CSS si no funcionó
            if not boton_continuar:
                selectores_btn = [
                    'button:has-text("Continuar")',
                    'button:has-text("Continue")',
                    'button[type="submit"]',
                    'button[class*="primary"]',
                ]
                for sel in selectores_btn:
                    try:
                        btn = page.locator(sel).first
                        if btn.count() and btn.is_visible(timeout=200):
                            btn.click(timeout=2000)
                            boton_continuar = True
                            print(f"[{n}] {perfil}: botón 'Continuar' pulsado ✓")
                            break
                    except Exception:
                        continue

            if toggle_activado or boton_continuar:
                exitosos += 1
            else:
                print(f"[{n}] {perfil}: no se pudo completar Fase 7")

        except Exception as e:
            print(f"[{n}] {perfil}: error - {e}")

        time.sleep(0.3)

    print(f"\n✓ Fase 7 completada: {exitosos}/{len(sesiones_validas)} ventana(s) con prueba 60 días activada")
    return exitosos


