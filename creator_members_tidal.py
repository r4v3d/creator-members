"""
Creador de cuentas TIDAL - Automatización por fases

Fases:
    1. Abrir ventanas en navegador modo incógnito
    2. Aceptar cookies en todas las ventanas
    3. Rellenar emails en formularios de login
    4. Pulsar botón 'Continuar'
    5. Rellenar contraseña, fecha de nacimiento, aceptar términos y pulsar 'Suscríbete'
    6. Verificar estado final, recuperar ventanas incompletas y navegar a link de suscripción
    7. Activar prueba de 60 días y pulsar 'Continuar' en página de planes

Características:
    - Detección de antibot/captcha (pausa manual para resolución)
    - Detección de error 403 CloudFront
    - Verificación rápida (<3s) después de cada fase
    - Recuperación automática de ventanas recargadas/incompletas
    - Navegación automática al link de suscripción
    - Activación automática de prueba de 60 días

Uso:
    python creator_tidal_accounts.py

Archivos requeridos:
    links.txt              - URLs de TIDAL (formato: URL, NombrePerfil)
    emails.txt             - Emails para registro (uno por línea)
    passwords.txt          - Contraseñas (una por línea, orden correspondiente)
    link subscription.txt  - URL de la página de suscripción de TIDAL
"""
