        remplazos = {
            'á': 'a', 'é': 'e', 'í': 'i', 'ó': 'o', 'ú': 'u',
            'ü': 'u', 'ñ': 'n'
        }
        for orig, dest in remplazos.items():
            t = t.replace(orig, dest)
        # Reemplazar entidades HTML comunes y normalizar espacios
        t = t.replace('&oacute;', 'o').replace('&html;', '').replace('&nbsp;', ' ')
