def fase_1_abrir_ventanas(playwright, sesiones: list[dict]) -> list[dict]:
    """
    Fase 1: Abre ventanas en modo incógnito usando Chrome REAL (no Chromium).
    CADA sesión tiene su propio contexto aislado.
    Retorna sesiones_actualizadas.
    """
    if not sesiones:
        print("No hay sesiones para abrir. Agrega URLs a links.txt")
        return sesiones

    # Buscar Chrome instalado
    chrome_path = encontrar_chrome()
    if not chrome_path:
        print("\n⚠ ERROR: No se encontró Google Chrome instalado.")
        print("  Instala Chrome desde: https://www.google.com/chrome/")
        print("  O especifica la ruta manualmente en el código.")
        return sesiones

    print(f"\n=== FASE 1: Abriendo {len(sesiones)} ventana(s) con Chrome REAL ===")
    print(f"Chrome detectado: {chrome_path}")
    print("Cada perfil usa contexto aislado (anti-detección)\n")

    # Argumentos anti-detección para Chrome real
    args_anti_deteccion = [
        "--incognito",
        "--disable-blink-features=AutomationControlled",
        "--disable-web-security",
        "--disable-features=IsolateOrigins,site-per-process",
        "--disable-site-isolation-trials",
        "--disable-dev-shm-usage",
        "--no-sandbox",
        "--disable-setuid-sandbox",
        "--disable-accelerated-2d-canvas",
        "--disable-gpu",
        "--window-size=1920,1080",
        "--start-maximized",
        "--force-device-scale-factor=1",
    ]

    browser = playwright.chromium.launch(
        headless=False,
        executable_path=chrome_path,
        args=args_anti_deteccion,
    )

    for sesion in sesiones:
        try:
            # Crear un NUEVO contexto para cada sesión (perfil aislado)
            context = browser.new_context()
            page = context.new_page()
            page.goto(sesion["url"], wait_until="domcontentloaded", timeout=30000)

            sesion["page"] = page
            sesion["browser"] = browser
            sesion["context"] = context  # Cada uno tiene su propio contexto

            print(f"[{sesion['n']}] {sesion['perfil']}: abierta en perfil aislado ✓")
            time.sleep(0.3)
        except Exception as e:
            print(f"[{sesion['n']}] Error abriendo {sesion['perfil']}: {e}")

    abiertas = len([s for s in sesiones if s.get("page")])
    print(f"\n✓ Fase 1 completada: {abiertas}/{len(sesiones)} perfil(es) abierto(s)")
    print("  Cada perfil está completamente aislado (cookies, almacenamiento independiente)")
    return sesiones


