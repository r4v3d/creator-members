def pausa_manual(subtitulo: str, segundos_sin_tty: float = 90.0) -> None:
    """Detiene el script hasta Enter (o espera fija si no hay consola interactiva)."""
    bar = "=" * 62
    print(f"\n{bar}\n  PAUSA MANUAL — {subtitulo}\n{bar}")
    print(
        "  El script queda detenido. Resuelve captchas/antibot en las ventanas.\n"
        "  Cuando esté todo listo, vuelve a ESTA ventana y pulsa Enter para seguir.\n"
    )
    try:
        if sys.stdin.isatty():
            input("  >>> Pulsa Enter para continuar el script <<<  ")
        else:
            print(f"  (Sin TTY: esperando {segundos_sin_tty:.0f}s y continuando…)\n")
            time.sleep(segundos_sin_tty)
    except EOFError:
        time.sleep(segundos_sin_tty)


def detectar_antibot_tid(page) -> bool:
    """Detecta pantalla de verificación humana / antibot de TIDAL."""
    try:
        if page.is_closed():
            return False
    except PWError:
        return False

    patrones = (
        re.compile(r"nos\s+aseguramos\s+de\s+que", re.I),
        re.compile(r"no\s+a\s+un\s+robot", re.I),
        re.compile(r"desliza\s+hacia\s+la\s+derecha", re.I),
        re.compile(r"velocidad\s+sobrehumana", re.I),
        re.compile(r"making\s+sure.*robot", re.I),
        re.compile(r"not\s+a\s+robot", re.I),
        re.compile(r"slide\s+to\s+(the\s+)?right", re.I),
        re.compile(r"superhuman\s+speed", re.I),
        re.compile(r"captcha", re.I),
        re.compile(r"recaptcha", re.I),
        re.compile(r"soy\s+humano", re.I),
        re.compile(r"i['']?m\s+human", re.I),
    )

    for frame in frames_visibles(page):
        for pat in patrones:
            try:
                loc = frame.get_by_text(pat)
                if loc.count() == 0:
                    continue
                if loc.first.is_visible(timeout=700):
                    return True
            except PWTimeout:
                continue
            except PWError:
                return False
            except Exception:
                continue
    return False


def detectar_error_403_cloudfront(page) -> bool:
    """Detecta página 403 de CloudFront ('Request blocked')."""
    try:
        if page.is_closed():
            return False
    except PWError:
        return False

    try:
        titulo = page.title()
        if re.search(r"403|request could not be satisfied", titulo, re.I):
            return True
    except Exception:
        pass

    patrones = (
        re.compile(r"403\s*ERROR", re.I),
        re.compile(r"the request could not be satisfied", re.I),
        re.compile(r"request blocked", re.I),
        re.compile(r"generated by cloudfront", re.I),
        re.compile(r"we can'?t connect to the server", re.I),
        re.compile(r"demasiado tr[aá]fico", re.I),
        re.compile(r"error 403", re.I),
        re.compile(r"access denied", re.I),
    )

    for frame in frames_visibles(page):
        for pat in patrones:
            try:
                loc = frame.get_by_text(pat)
                if loc.count() == 0:
                    continue
                if loc.first.is_visible(timeout=700):
                    return True
            except PWTimeout:
                continue
            except PWError:
                return False
            except Exception:
                continue

        try:
            txt = frame.locator("body").inner_text(timeout=1500)
            if re.search(r"403\s*ERROR|generated by cloudfront|request blocked|access denied", txt, re.I):
                return True
        except Exception:
            continue
    return False


def detectar_bloqueo_tid(page) -> str | None:
    """Retorna tipo de bloqueo detectado o None si está OK."""
    if detectar_antibot_tid(page):
        return "antibot"
    if detectar_error_403_cloudfront(page):
        return "403 CloudFront"
    return None


def verificar_bloqueos(sesiones: list[dict]) -> list[tuple[int, str, str]]:
    """Verifica si hay bloqueos en las ventanas. Retorna lista de (n, perfil, tipo_bloqueo)."""
    bloqueadas = []
    for sesion in sesiones:
        page = sesion.get("page")
        if not page:
            continue
        try:
            tipo = detectar_bloqueo_tid(page)
            if tipo:
                bloqueadas.append((sesion["n"], sesion["perfil"], tipo))
        except Exception:
            continue
    return bloqueadas


def manejar_bloqueos_y_completitud(
    sesiones: list[dict],
    momento: str,
    fase_verificar: str | None = None,
    datos_verificacion: dict | None = None,
    max_espera_verificacion: float = 3.0,
) -> dict:
    """
    Detecta bloqueos y verifica que la fase anterior se completó correctamente.

    Retorna dict con:
        - bloqueos_resueltos: bool
        - fase_completa: bool
        - ventanas_bloqueadas: list
        - ventanas_incompletas: list
    """
    resultado = {
        "bloqueos_resueltos": True,
        "fase_completa": True,
        "ventanas_bloqueadas": [],
        "ventanas_incompletas": [],
    }

    # 1. Verificar bloqueos
    bloqueadas = verificar_bloqueos(sesiones)
    if bloqueadas:
        tipos = {m for _, _, m in bloqueadas}
        if "403 CloudFront" in tipos and "antibot" in tipos:
            titulo = "BLOQUEO TIDAL (antibot y 403 CloudFront)"
        elif "403 CloudFront" in tipos:
            titulo = "ERROR 403 CLOUDFRONT"
        else:
            titulo = "VERIFICACIÓN ANTI-ROBOT (TIDAL)"

        bar = "=" * 62
        print(f"\n{bar}\n  {titulo} — {momento}\n{bar}")
        for n, perfil, motivo in bloqueadas:
            print(f"   • Ventana [{n}] — {perfil} ({motivo})")
        print(f"\n  Total: {len(bloqueadas)} ventana(s) afectada(s)")

        # Pausa manual para que usuario resuelva
        pausa_manual(f"Resuelve el {titulo} en las ventanas marcadas")

    # 2. Verificar completitud de fase (si se especificó)
    incompletas = []
    if fase_verificar:
        incompletas = verificar_completitud_fase(sesiones, fase_verificar, datos_verificacion)

    # 3. Verificación ultra-rápida combinada (<3s total)
    print(f"\n  Verificando {len(sesiones)} ventanas...")
    inicio = time.time()
    ventanas_revisadas = 0
    errores_bloqueo = []
    errores_completitud = []

    # Calcular tiempo máximo por ventana para cumplir con el límite total
    tiempo_max_por_ventana = max_espera_verificacion / max(len(sesiones), 1)

    for sesion in sesiones:
        if time.time() - inicio > max_espera_verificacion:
            break

        page = sesion.get("page")
        if not page:
            continue

        n, perfil = sesion["n"], sesion["perfil"]
        ventanas_revisadas += 1
        status_parts = []

        try:
            # Verificar bloqueo (ultra-rápido)
            tipo_bloqueo = detectar_bloqueo_tid(page)
            if tipo_bloqueo:
                errores_bloqueo.append((n, perfil, tipo_bloqueo))
                status_parts.append(f"BLOQUEO: {tipo_bloqueo}")

            # Verificar completitud si aplica (ultra-rápido)
            if fase_verificar == "fase3_email":
                email_esperado = datos_verificacion.get("emails", [])[n - 1] if datos_verificacion and n <= len(datos_verificacion.get("emails", [])) else ""
                if email_esperado and not verificar_email_rellenado(page, email_esperado):
                    errores_completitud.append((n, perfil, "email no rellenado"))
                    status_parts.append("FALTA: email")
                elif not email_esperado:
                    status_parts.append("FALTA: email asignado")

            elif fase_verificar == "fase4_continuar":
                if not verificar_pagina_registro(page):
                    errores_completitud.append((n, perfil, "no avanzó a registro"))
                    status_parts.append("FALTA: página registro")

            elif fase_verificar == "fase5_registro":
                estado = verificar_registro_completo(page)
                faltantes = []
                if not estado["password"]:
                    faltantes.append("contraseña")
                if not estado["fecha_nacimiento"]:
                    faltantes.append("fecha")
                if not estado["terminos"]:
                    faltantes.append("términos")
                if not estado["boton_suscribirse"]:
                    faltantes.append("botón bloqueado")
                    status_parts.append("BOTÓN DESHABILITADO (tono oscuro)")
                if faltantes and "botón bloqueado" not in faltantes:
                    errores_completitud.append((n, perfil, f"falta: {', '.join(faltantes)}"))
                    status_parts.append(f"FALTA: {', '.join(faltantes)}")

            if not status_parts:
                status_parts.append("OK ✓")

            # Solo mostrar si hay problemas (para ser más rápido visualmente)
            if len(status_parts) > 1 or status_parts[0] != "OK ✓":
                print(f"   • [{n}] {perfil}: {' | '.join(status_parts)}")

        except Exception:
            # Silenciar errores individuales para velocidad
            pass

        # Pausa mínima proporcional
        if tiempo_max_por_ventana > 0.05:
            time.sleep(tiempo_max_por_ventana * 0.3)  # Usar solo 30% del tiempo asignado

    # Mostrar OK para ventanas que no tuvieron problemas (una sola línea)
    if ventanas_revisadas > 0 and not errores_bloqueo and not errores_completitud:
        print(f"   • Todas las {ventanas_revisadas} ventanas: OK ✓")

    # 4. Resumen
    print(f"\n  Verificación completada ({ventanas_revisadas} ventanas)")

    if errores_bloqueo:
        resultado["bloqueos_resueltos"] = False
        resultado["ventanas_bloqueadas"] = errores_bloqueo
        print(f"\n  ⚠ {len(errores_bloqueo)} ventana(s) aún con bloqueos:")
        for n, perfil, motivo in errores_bloqueo:
            print(f"     • [{n}] {perfil}: {motivo}")

    if errores_completitud:
        resultado["fase_completa"] = False
        resultado["ventanas_incompletas"] = errores_completitud
        print(f"\n  ⚠ {len(errores_completitud)} ventana(s) con fase incompleta:")
        for n, perfil, motivo in errores_completitud:
            print(f"     • [{n}] {perfil}: {motivo}")

    if not errores_bloqueo and not errores_completitud:
        print(f"\n  ✓ Todas las ventanas OK - sin bloqueos ni faltantes")

    return resultado


