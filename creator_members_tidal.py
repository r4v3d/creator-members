def verificar_email_rellenado(page, email_esperado: str) -> bool:
    """Verifica ultra-rápido que el campo de email tenga el valor correcto."""
    try:
        if page.is_closed():
            return False
    except PWError:
        return False

    # Selector más común y rápido
    try:
        loc = page.locator('input[type="email"], input[name="email"], input[autocomplete="email"]').first
        if loc.count() and loc.is_visible(timeout=150):
            valor = loc.input_value(timeout=200)
            return valor and valor.lower() == email_esperado.lower()
    except Exception:
        pass
    return False


def verificar_pagina_registro(page) -> bool:
    """Verifica ultra-rápido que estamos en la página de registro."""
    try:
        if page.is_closed():
            return False
    except PWError:
        return False

    # Solo buscar el campo de contraseña (más rápido)
    try:
        loc = page.locator('input[type="password"]').first
        if loc.count() and loc.is_visible(timeout=150):
            return True
    except Exception:
        pass
    return False


def verificar_registro_completo(page) -> dict[str, bool]:
    """Verifica ultra-rápido el estado del formulario de registro."""
    estado = {"password": False, "fecha_nacimiento": False, "terminos": False, "boton_suscribirse": False}

    try:
        if page.is_closed():
            return estado
    except PWError:
        return estado

    # Verificar contraseña (selector directo)
    try:
        loc = page.locator('input[type="password"]').first
        if loc.count():
            val = loc.input_value(timeout=150)
            if val and len(val) > 0:
                estado["password"] = True
    except Exception:
        pass

    # Verificar fecha de nacimiento (solo 1 select con valor)
    try:
        selects = page.locator('select').all()
        con_valor = sum(1 for s in selects if s.input_value(timeout=100).strip())
        if con_valor >= 1:
            estado["fecha_nacimiento"] = True
    except Exception:
        pass

    # Verificar términos aceptados
    try:
        loc = page.locator('input[type="checkbox"]').first
        if loc.count() and loc.is_checked():
            estado["terminos"] = True
    except Exception:
        pass

    # Verificar botón Suscríbete (habilitado = campos completos, tono claro)
    try:
        btn = page.locator('button:has-text("Suscríbete"), button:has-text("Subscribe")').first
        if btn.count() and btn.is_visible(timeout=150):
            # Verificar si el botón está habilitado (no tiene atributo disabled)
            is_enabled = btn.is_enabled(timeout=100)
            # El botón habilitado aparece en tono claro/blanco
            # El botón deshabilitado aparece en tono oscuro/gris
            estado["boton_suscribirse"] = is_enabled
    except Exception:
        pass

    return estado


def verificar_completitud_fase(sesiones: list[dict], fase: str, datos: dict | None = None) -> list[tuple[int, str, str]]:
    """
    Verifica que todas las ventanas hayan completado la fase indicada.
    Retorna lista de ventanas incompletas: (n, perfil, razon).
    """
    incompletas = []

    for sesion in sesiones:
        page = sesion.get("page")
        if not page:
            continue

        n, perfil = sesion["n"], sesion["perfil"]

        try:
            if fase == "fase3_email":
                email_esperado = datos.get("emails", [])[n - 1] if datos and n <= len(datos.get("emails", [])) else ""
                if email_esperado and not verificar_email_rellenado(page, email_esperado):
                    incompletas.append((n, perfil, "email no rellenado o incorrecto"))
                elif not email_esperado:
                    incompletas.append((n, perfil, "no hay email asignado"))

            elif fase == "fase4_continuar":
                if not verificar_pagina_registro(page):
                    incompletas.append((n, perfil, "no se avanzó a página de registro"))

            elif fase == "fase5_registro":
                estado = verificar_registro_completo(page)
                faltantes = []
                if not estado["password"]:
                    faltantes.append("contraseña")
                if not estado["fecha_nacimiento"]:
                    faltantes.append("fecha_nacimiento")
                if not estado["terminos"]:
                    faltantes.append("términos")
                if not estado["boton_suscribirse"]:
                    faltantes.append("botón_suscribirse")

                if faltantes:
                    incompletas.append((n, perfil, f"incompleto: {', '.join(faltantes)}"))

        except Exception as e:
            incompletas.append((n, perfil, f"error al verificar: {e}"))

    return incompletas


def detectar_estado_ventana(page, email_esperado: str = "", password: str = "") -> dict[str, bool]:
    """
    Detecta en qué fase está una ventana y qué campos están completos.
    Retorna estado de cada componente.
    """
    estado = {
        "pagina_login": False,        # Estamos en página de login (campo email visible)
        "email_rellenado": False,     # Email está en el campo
        "pagina_registro": False,     # Estamos en página de registro (campo password visible)
        "password_rellenada": False,  # Password tiene valor
        "fecha_rellenada": False,     # Fecha tiene valor
        "terminos_aceptados": False,  # Checkbox marcado
        "boton_suscribirse_habilitado": False,  # Botón listo para pulsar
        "registro_completado": False, # Ya pasó el registro (no hay formulario)
    }

    try:
        if page.is_closed():
            return estado
    except PWError:
        return estado

    try:
        # Detectar página de login (campo email visible)
        email_field = page.locator('input[type="email"], input[name="email"]').first
        if email_field.count() and email_field.is_visible(timeout=100):
            estado["pagina_login"] = True
            val = email_field.input_value(timeout=100)
            if val and email_esperado and val.lower() == email_esperado.lower():
                estado["email_rellenado"] = True
            elif val:
                estado["email_rellenado"] = True  # Tiene algún email

        # Detectar página de registro (campo password visible)
        pwd_field = page.locator('input[type="password"]').first
        if pwd_field.count() and pwd_field.is_visible(timeout=100):
            estado["pagina_registro"] = True
            val = pwd_field.input_value(timeout=100)
            if val and len(val) > 0:
                estado["password_rellenada"] = True

        # Verificar fecha (selects con valores)
        try:
            selects = page.locator('select').all()
            con_valor = sum(1 for s in selects if s.input_value(timeout=100).strip())
            if con_valor >= 1:
                estado["fecha_rellenada"] = True
        except Exception:
            pass

        # Verificar términos
        try:
            cb = page.locator('input[type="checkbox"]').first
            if cb.count() and cb.is_checked():
                estado["terminos_aceptados"] = True
        except Exception:
            pass

        # Verificar botón Suscríbete (inlineado para evitar dependencia de orden)
        try:
            btn = page.locator('button:has-text("Suscríbete"), button:has-text("Subscribe")').first
            if btn.count() and btn.is_visible(timeout=100):
                estado["boton_suscribirse_habilitado"] = btn.is_enabled(timeout=100)
        except Exception:
            pass

        # Detectar si ya pasó el registro (no hay formularios típicos)
        if not estado["pagina_login"] and not estado["pagina_registro"]:
            # Podría ser página post-registro o de suscripción
            estado["registro_completado"] = True

    except Exception:
        pass

    return estado


def recuperar_ventana(sesion: dict, email: str, password: str, max_intentos: int = 2) -> bool:
    """
    Recupera una ventana que se recargó o quedó incompleta.
    Re-ejecuta las fases necesarias según el estado detectado.
    Retorna True si la ventana está lista para ir al link de suscripción.
    """
    page = sesion.get("page")
    if not page:
        return False

    n, perfil = sesion["n"], sesion["perfil"]

    for intento in range(max_intentos):
        estado = detectar_estado_ventana(page, email, password)

        # Caso 1: Estamos en página de login y falta email
        if estado["pagina_login"] and not estado["email_rellenado"]:
            if email and rellenar_email_login(page, email):
                time.sleep(0.3)
                pulsar_boton_por_texto(page, (
                    re.compile(r"^\s*continuar\s*$", re.I),
                    re.compile(r"^\s*continue\s*$", re.I),
                ))
                time.sleep(1.0)  # Esperar carga de página de registro
                continue

        # Caso 2: Estamos en página de registro pero incompleta
        if estado["pagina_registro"]:
            # Rellenar password si falta
            if not estado["password_rellenada"] and password:
                try:
                    loc = page.locator('input[type="password"]').first
                    if loc.count():
                        loc.fill(password, timeout=1000, force=True)
                        time.sleep(0.2)
                except Exception:
                    pass

            # Rellenar fecha si falta
            if not estado["fecha_rellenada"]:
                import random
                dia, mes, año = str(random.randint(1, 28)), str(random.randint(1, 12)), str(random.randint(1970, 2005))
                try:
                    selects = page.locator('select').all()
                    for i, sel in enumerate(selects[:3]):
                        try:
                            val = sel.input_value(timeout=100)
                            if not val or not val.strip():
                                sel.select_option([dia, mes, año][i])
                                time.sleep(0.1)
                        except Exception:
                            continue
                except Exception:
                    pass

            # Aceptar términos si faltan (usando force=True para mayor fiabilidad)
            if not estado["terminos_aceptados"]:
                try:
                    # Intentar primero la función mejorada
                    if not aceptar_terminos_privacidad(page):
                        # Fallback: marcar todos los checkboxes no marcados
                        checkboxes = page.locator('input[type="checkbox"]').all()
                        for cb in checkboxes:
                            try:
                                if cb.is_visible(timeout=200) and not cb.is_checked():
                                    cb.check(timeout=2000, force=True)
                                    time.sleep(0.2)
                            except Exception:
                                continue
                except Exception:
                    pass

            # Verificar si el botón se habilitó (inlineado)
            time.sleep(0.3)
            habilitado = False
            try:
                btn = page.locator('button:has-text("Suscríbete"), button:has-text("Subscribe")').first
                if btn.count() and btn.is_visible(timeout=200):
                    habilitado = btn.is_enabled(timeout=200)
            except Exception:
                pass
            
